"""
GAVEL — Privilege Log Generator
=================================
Scans a folder of documents and generates a privilege log spreadsheet.
Uses Claude AI to write a one-line description of each document.

SETUP (run once):
    Mac:     /usr/local/bin/pip3 install anthropic pypdf openpyxl
    Windows: pip install anthropic pypdf openpyxl

USAGE:
    Mac:     python3 privilege_log.py
    Windows: python privilege_log.py

    The script will ask you for your API key, folder, and options.
    No editing required.

OUTPUT:
    An Excel spreadsheet (.xlsx) with these columns:
    - Document Number
    - Filename
    - File Type
    - Date Modified
    - File Size
    - Description (generated by Claude AI)
    - Privilege Claimed
    - Notes
"""

